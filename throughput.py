@torch.no_grad()
def throughput(args):
    # might as well try to validate something
    args.prefetcher = not args.no_prefetcher

    # create model
    if args.model.startswith("convnext"):
        _logger.info('Creating convnext model')
        model = create_model(
            args.model, 
            pretrained=False, 
            num_classes=args.num_classes, 
            drop_path_rate=0,
            layer_scale_init_value=1e-6,
            head_init_scale=1.0,
            drop_rate=0,
            )
    elif args.model.startswith("poolformer"):
        _logger.info('Creating poolformer model')
        model = create_model(
            args.model,
            pretrained=args.pretrained,
            num_classes=args.num_classes,
            drop_rate=0,
            drop_connect_rate=0,  # DEPRECATED, use drop_path
            drop_path_rate=0,
            drop_block_rate=0,
            global_pool=None,
            bn_tf=False,
            bn_momentum=None,
            bn_eps=None,
            scriptable=args.torchscript,
            checkpoint_path='')
    elif args.model.startswith("vanillanet"):
        _logger.info('Creating vanillanet model')
        model = create_model(
            args.model, 
            pretrained=False,
            num_classes=args.num_classes, 
            act_num=3,
            drop_rate=0,
            deploy=True,
            )
    else:
        _logger.info('Creating vit, swin, doit model')
        model = create_model(
            args.model, 
            pretrained=False, 
            num_classes=args.num_classes, 
            drop_path_rate=0,
            drop_rate=0,
            img_size=args.img_size
            )
        
    if args.num_classes is None:
        assert hasattr(model, 'num_classes'), 'Model must have `num_classes` attr if not set on cmd line/config.'
        args.num_classes = model.num_classes

    param_count = sum([m.numel() for m in model.parameters()])
    _logger.info('Model %s created, param count: %d' % (args.model, param_count))

    data_config = resolve_data_config(vars(args), model=model, use_test_size=True, verbose=True)
    test_time_pool = False

    model = model.cuda()

    dataset = create_dataset(
        root=args.data, name=args.dataset, split=args.split,
        download=args.dataset_download, load_bytes=args.tf_preprocessing, class_map=args.class_map)

    crop_pct = 1.0 if test_time_pool else data_config['crop_pct']
    loader = create_loader(
        dataset,
        input_size=data_config['input_size'],
        batch_size=args.batch_size,
        use_prefetcher=args.prefetcher,
        interpolation=data_config['interpolation'],
        mean=data_config['mean'],
        std=data_config['std'],
        num_workers=args.workers,
        crop_pct=crop_pct,
        pin_memory=args.pin_mem,
        tf_preprocessing=args.tf_preprocessing)

    model.eval()

    for idx, (images, _) in enumerate(loader):
        images = images.cuda(non_blocking=True)
        batch_size = images.shape[0]
        for i in range(50):
            model(images)
        torch.cuda.synchronize()
        _logger.info(f"throughput averaged with 30 times")
        tic1 = time.time()
        for i in range(30):
            model(images)
        torch.cuda.synchronize()
        tic2 = time.time()
        _logger.info(f"batch_size {batch_size} throughput {30 * batch_size / (tic2 - tic1)}")
        return


def main():
    setup_default_logging()
    args = parser.parse_args()
    throughput(args)


if __name__ == '__main__':
    main()